void PA_SetTextFont(bool screen, u8 font) {
	PA_font[screen] = font;
	if (font == 0) {
		PA_textmap[screen] = (u16*)PA_font_Map;
		PA_texttiles[screen] = (u16*)PA_font_Tiles;
	}
	else {
		PA_textmap[screen] = (u16*)PA_font2_Map;
		PA_texttiles[screen] = (u16*)PA_font2_Tiles;
	}

	PA_InitText(screen, PAbgtext[screen]); // On recharge le tout en mémoire
}

void PA_SetTextCustomFont(bool screen, void* tiles, void* map) {
	PA_font[screen] = -1;

	PA_textmap[screen] = (u16*)map;
	PA_texttiles[screen] = (u16*)tiles;


	PA_InitText(screen, PAbgtext[screen]); // On recharge le tout en mémoire
}


void PA_SetTextCol(bool screen, u8 r, u8 g, u8 b) {
	textcol[screen] = PA_RGB(r, g, b); 
	if (PA_font[screen] == 0) BG_PALETTE[255 + (screen * 512)] = textcol[screen];
	else if (PA_font[screen] == 1) { // On charge pour un dégradé...
		BG_PALETTE[255 + (screen * 512)] = PA_RGB(r, g, b);
		r -= r >> 2; g -= g >> 2;  b -= b >> 2;
		BG_PALETTE[254 + (screen * 512)] = PA_RGB(r, g, b);
		r -= r >> 3; g -= g >> 3;  b -= b >> 3;
		BG_PALETTE[253 + (screen * 512)] = PA_RGB(r, g, b);
	}
}



