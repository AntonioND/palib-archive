void PA_SplashBlue(void){

	PA_SetBrightness(0, 31); 	PA_SetBrightness(1, 31);
	
	s16 scroll0 = 0;
	s16 scroll1 = 256;

	PA_LoadTiledBg(0, 3, Splash2);	
	PA_LoadTiledBg(1, 3, Splash2);		
	
	PA_BGScrollX(1, 3, scroll1);
	
	s8 i;
	for (i = 31; i >= 0; i--) {
	   PA_SetBrightness(0, i);
	   PA_SetBrightness(1, i);	   
	   PA_WaitForVBL();
	}
	
	
	   

	// Infinite loop to keep the program running
	while ((!Pad.Newpress.Anykey)&&(!Stylus.Newpress))
	{
	   
	   scroll0++; scroll0 &= 511;
	   scroll1++; scroll1 &= 511;
	   PA_BGScrollX(0, 3, scroll0);
	   PA_BGScrollX(1, 3, scroll1);	   
	   
		PA_WaitForVBL();
	}
	

// fade out
	for (i = 0; i < 31; i++) {
	   PA_SetBrightness(0, i);
	   PA_SetBrightness(1, i);	   
	   PA_WaitForVBL();
	}
	
	// Reset everything
	PA_ResetSpriteSys(); 
	PA_ResetBgSys(); 
	PA_SetBrightness(0, 0); // Bacl to normal
	PA_SetBrightness(1, 0); // Back to normal
	   
	   
}


